# Use spaces NOT tabs
# 4 spaces = 1 tab
# Lines length = 79 characters
# If expression over 1 line, the following line should indent by 4 spaces
# Functions & classes are separated by 2 black lines
# Inside class, methods should be separated by one blank line
# No space for list index, function call, keyword argument
# One space on both sides. var = 1

# Naming
# Function, variable, attributed: lowercase_underscore
# Protected instance attributes : _leading_underscore
# Private instance attribautes  : __double_leading_underscore
# Classes and exceptions        : CapitalizedWord
# Module-level constant         : ALL_CAPS
# Instance methods use `self` as first parameter
# Class methods use `cls` as name of first parameter

# Expression & Statements
# Use inline negation `if a is not b`
# Don't use `len` to check empty, use variable directly
# Avoid single-line if, for, except
# `import` should always at Top
# Use absolute value for `import`
# Import order: standard library -> third party -> customized. Use alphabetical order under each section
